public class Item {

    String name;
    double price;

    public String getName()
    {
        return this.name;
    }
    public void setName(String value)
    {
        this.name = value;
    }

    public double getPrice()
    {
        return this.price;
    }
    public void setPrice(double value)
    {
        this.price = value;
    }
} 
